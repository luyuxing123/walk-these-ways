import torch


import torch


def split_and_pad_trajectories(tensor: torch.Tensor, dones: torch.Tensor):
    """ Splits trajectories at done indices. Then concatenates them and padds with zeros up to the length og the longest trajectory.
    Returns masks corresponding to valid parts of the trajectories
    Example:
        Input: [ [a1, a2, a3, a4 | a5, a6],
                 [b1, b2 | b3, b4, b5 | b6]
                ]

        Output:[ [a1, a2, a3, a4], | [  [True, True, True, True],
                 [a5, a6, 0, 0],   |    [True, True, False, False],
                 [b1, b2, 0, 0],   |    [True, True, False, False],
                 [b3, b4, b5, 0],  |    [True, True, True, False],
                 [b6, 0, 0, 0]     |    [True, False, False, False],
                ]                  | ]

    Assumes that the inputy has the following dimension order: [time, number of envs, aditional dimensions]
    """
    dones = dones.clone()
    dones[-1] = 1
    # Permute the buffers to have order (num_envs, num_transitions_per_env, ...), for correct reshaping
    flat_dones = dones.transpose(1, 0).reshape(-1, 1)  # 铺平成一维向量，确定哪些状态是终止状态
    done_indices = torch.cat((flat_dones.new_tensor(
        [-1], dtype=torch.int64), flat_dones.nonzero()[:, 0]))  # 一维向量：记录了done状态的位置索引
    trajectory_lengths = done_indices[1:] - \
        done_indices[:-1]  # 根据连续两个终止状态的差值确定有多少条轨迹和每条轨迹的长度 shape:[num_trajs]
    trajectory_lengths_list = trajectory_lengths.tolist()  # 得到轨迹列表，存储所有轨迹的长度

    # trajectories_shape: a list of [traj_length, obs_dim]
    trajectories = torch.split(tensor.transpose(1, 0).flatten(
        0, 1), trajectory_lengths_list)  # 按轨迹长度划分好每段轨迹的观测状态

    # padded_trajectories_shape: [longest_traj_length, num_traj, obs_dim]
    padded_trajectories = torch.nn.utils.rnn.pad_sequence(
        trajectories)  # 填充每条轨迹为相同长度

    # masks_shape: [longest_traj_length, num_traj]
    trajectory_masks = trajectory_lengths > torch.arange(
        0, tensor.shape[0], device=tensor.device).unsqueeze(1)

    return padded_trajectories, trajectory_masks


def unpad_trajectories(trajectories, masks):
    """ Does the inverse operation of  split_and_pad_trajectories()
    """
    # Need to transpose before and after the masking to have proper reshaping
    return trajectories.transpose(1, 0)[masks.transpose(1, 0)].view(-1, trajectories.shape[0], trajectories.shape[-1]).transpose(1, 0)


